    // DOM Elements
    const loader = document.getElementById('paroLoader');
    const lectureContainer = document.getElementById('lectureContainer');
    const subjectTitle = document.getElementById('subjectTitle');
    const modal = document.getElementById('paroModal');
    const viewerBody = document.getElementById('viewerBody');
    const viewerTitle = document.getElementById('viewerTitle');
    const lectureCount = document.getElementById('lectureCount');
    const totalDuration = document.getElementById('totalDuration');
    const progressValue = document.getElementById('progressValue');
    const prevBtn = document.getElementById('prevBtn');
    const nextBtn = document.getElementById('nextBtn');
    const modalInfo = document.getElementById('modalInfo');
    
    let currentSubject = '';
    let currentLectureIndex = 0;
    let currentSubjectData = [];

    // Initialize with Physics by default
    document.addEventListener('DOMContentLoaded', function() {
      setTimeout(() => {
        loadSubject('Physics');
      }, 500);
    });

    function loadSubject(subject) {
      // Update UI state
      currentSubject = subject;
      subjectTitle.textContent = subject;
      lectureContainer.innerHTML = '';
      
      // Update active subject in sidebar
      document.querySelectorAll('.subject-item').forEach(item => {
        item.classList.remove('active');
      });
      
      event.currentTarget.classList.add('active');
      
      // Show loader
      loader.classList.add('active');
      
      // Simulate loading delay
      setTimeout(() => {
        loader.classList.remove('active');
        currentSubjectData = paroData[subject];
        
        // Update stats
        lectureCount.textContent = currentSubjectData.length;
        
        // Calculate total duration
        let totalMinutes = 0;
        currentSubjectData.forEach(lecture => {
          if (lecture.duration) {
            const mins = parseInt(lecture.duration);
            if (!isNaN(mins)) totalMinutes += mins;
          }
        });
        
        const hours = Math.floor(totalMinutes / 60);
        const minutes = totalMinutes % 60;
        totalDuration.textContent = hours > 0 ? `${hours}h ${minutes}m` : `${totalMinutes}m`;
        
        // Random progress for demo
        progressValue.textContent = `${Math.floor(Math.random() * 30) + 20}%`;
        
        // Generate lecture cards
        currentSubjectData.forEach((item, index) => {
          const card = document.createElement('div');
          card.className = 'lecture-card';
          card.innerHTML = `
            <div class="lecture-header">
              <div class="lecture-number">${index + 1}</div>
              <div class="lecture-type">${item.type.toUpperCase()}</div>
            </div>
            <h3 class="lecture-title">${item.title}</h3>
            <div class="lecture-duration">
              <i class="fas fa-clock"></i> ${item.duration}
            </div>
            <div class="lecture-actions">
              <button class="btn-primary" onclick="openViewer('${subject}', ${index})">
                <i class="fas fa-play"></i> Play Lecture
              </button>
              <button class="btn-secondary">
                <i class="fas fa-bookmark"></i> Save
              </button>
            </div>
          `;
          lectureContainer.appendChild(card);
        });
      }, 1500);
    }

    function openViewer(subject, index) {
      currentSubject = subject;
      currentLectureIndex = index;
      currentSubjectData = paroData[subject];
      
      const lecture = currentSubjectData[index];
      viewerTitle.textContent = lecture.title;
      
      // Create video element
      viewerBody.innerHTML = `
        <video class="modal-media" controls autoplay>
          <source src="${lecture.src}" type="video/mp4">
          Your browser does not support the video tag.
        </video>
      `;
      
      // Update modal info
      modalInfo.textContent = `Lecture ${index + 1} of ${currentSubjectData.length} â€¢ ${lecture.duration}`;
      
      // Update navigation buttons
      prevBtn.disabled = index === 0;
      nextBtn.disabled = index === currentSubjectData.length - 1;
      
      prevBtn.onclick = () => navigateLecture(-1);
      nextBtn.onclick = () => navigateLecture(1);
      
      // Show modal
      modal.classList.add('active');
      
      // Add keyboard navigation
      document.addEventListener('keydown', handleKeyNavigation);
    }

    function closeViewer() {
      modal.classList.remove('active');
      document.removeEventListener('keydown', handleKeyNavigation);
      
      // Pause video if playing
      const video = viewerBody.querySelector('video');
      if (video) video.pause();
    }

    function navigateLecture(direction) {
      const newIndex = currentLectureIndex + direction;
      
      if (newIndex >= 0 && newIndex < currentSubjectData.length) {
        closeViewer();
        setTimeout(() => {
          openViewer(currentSubject, newIndex);
        }, 300);
      }
    }

    function handleKeyNavigation(e) {
      if (!modal.classList.contains('active')) return;
      
      switch(e.key) {
        case 'ArrowLeft':
          if (currentLectureIndex > 0) navigateLecture(-1);
          break;
        case 'ArrowRight':
          if (currentLectureIndex < currentSubjectData.length - 1) navigateLecture(1);
          break;
        case 'Escape':
          closeViewer();
          break;
        case ' ':
          e.preventDefault();
          const video = viewerBody.querySelector('video');
          if (video) {
            video.paused ? video.play() : video.pause();
          }
          break;
      }
    }

    // Close modal when clicking outside
    modal.addEventListener('click', function(e) {
      if (e.target === modal) {
        closeViewer();
      }
    });