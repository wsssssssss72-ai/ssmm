    // DOM Elements
    const subjectTitle = document.getElementById('subjectTitle');
    const lectureContainer = document.getElementById('lectureContainer');
    const subjectChips = document.querySelectorAll('.subject-chip');

    // Load Subject Function
    function loadSubject(subject) {
      // Update active chip
      subjectChips.forEach(chip => {
        chip.classList.remove('active');
        if (chip.querySelector('.subject-name').textContent === subject) {
          chip.classList.add('active');
        }
      });
      
      // Update title
      subjectTitle.textContent = `${subject} Masterclass`;
      
      // Clear container with animation
      lectureContainer.innerHTML = '';
      lectureContainer.style.opacity = '0.5';
      
      // Show loading state
      setTimeout(() => {
        lectureContainer.innerHTML = '';
        lectureContainer.style.opacity = '1';
        
        if (courseData[subject]) {
          courseData[subject].forEach((lecture, index) => {
            const card = document.createElement('div');
            card.className = 'lecture-card animate-in';
            card.style.animationDelay = `${index * 0.1}s`;
            
            card.innerHTML = `
              <div class="card-badge">${lecture.type.toUpperCase()}</div>
              <div class="card-media">
                <div class="play-overlay">
                  <div class="play-btn" onclick="openPlayer('${lecture.title}', '${lecture.type}', '${lecture.src}')">
                    <i class="fas fa-play"></i>
                  </div>
                </div>
                <i class="fas fa-play-circle" style="font-size: 3rem; color: rgba(255,255,255,0.2);"></i>
              </div>
              <div class="card-content">
                <h3 class="card-title">${lecture.title}</h3>
                <p style="color: var(--text-dim); font-size: 0.9rem; line-height: 1.5;">
                  ${lecture.description}
                </p>
                <div class="card-meta">
                  <div class="meta-item">
                    <i class="far fa-clock"></i>
                    <span>${lecture.duration}</span>
                  </div>
                  <div class="meta-item">
                    <i class="far fa-play-circle"></i>
                    <span>Watch Now</span>
                  </div>
                </div>
              </div>
            `;
            
            lectureContainer.appendChild(card);
          });
        }
      }, 300);
    }

    // Open Player Function
    function openPlayer(title, type, src) {
      if (!src || src.trim() === '') {
        showNotification('This lecture is currently unavailable. Please try another one.', 'warning');
        return;
      }
      
      document.getElementById('playerTitle').textContent = title;
      const playerBody = document.getElementById('playerBody');
      
      if (type === 'video') {
        playerBody.innerHTML = `
          <video controls autoplay style="width:100%;height:100%;">
            <source src="${src}" type="video/mp4">
            Your browser does not support the video tag.
          </video>
        `;
      } else {
        playerBody.innerHTML = `<iframe src="${src}" style="width:100%;height:100%;border:none;"></iframe>`;
      }
      
      document.getElementById('playerModal').style.display = 'flex';
      document.body.style.overflow = 'hidden';
    }

    // Close Player Function
    function closePlayer() {
      document.getElementById('playerModal').style.display = 'none';
      document.body.style.overflow = 'auto';
      
      const video = document.querySelector('#playerBody video');
      if (video) {
        video.pause();
      }
    }

    // Notification Function
    function showNotification(message, type = 'info') {
      const notification = document.createElement('div');
      notification.style.cssText = `
        position: fixed;
        top: 100px;
        right: 20px;
        background: ${type === 'warning' ? 'rgba(255, 77, 141, 0.9)' : 'rgba(0, 212, 255, 0.9)'};
        color: white;
        padding: 1rem 1.5rem;
        border-radius: var(--radius-lg);
        z-index: 1001;
        animation: slideUp 0.3s ease;
        backdrop-filter: blur(10px);
        border: 1px solid rgba(255,255,255,0.2);
        max-width: 300px;
      `;
      notification.textContent = message;
      document.body.appendChild(notification);
      
      setTimeout(() => {
        notification.style.opacity = '0';
        notification.style.transform = 'translateX(100px)';
        setTimeout(() => notification.remove(), 300);
      }, 3000);
    }

    // Close modal on outside click
    document.getElementById('playerModal').addEventListener('click', (e) => {
      if (e.target.id === 'playerModal') {
        closePlayer();
      }
    });

    // Keyboard shortcuts
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape') {
        closePlayer();
      }
    });

